    public void buscarxCodigo(String cod){
    
        if(!tablaPacientes.Encontrado(cod)){
        
            JOptionPane.showMessageDialog(null, "Codigo no encontrado", "Error :(", JOptionPane.ERROR_MESSAGE);
            
        } else {
        
            paciente = tablaPacientes.BuscarPaciente(cod);
            
            codigoM.setText(paciente.getCodigoPaciente());
            nombreM.setText(paciente.getNombres());
            apellidoM.setText(paciente.getApellidos());
            sexoM.setText(paciente.getSexo());
            edadM.setText(paciente.getEdad());
            estadocivilM.setSelectedItem(paciente.getEstadoCivil());
            telefonoM.setText(paciente.getTelefono());
            direccionM.setText(paciente.getDireccion());
            emailM.setText(paciente.getEmail());
        }
    }
