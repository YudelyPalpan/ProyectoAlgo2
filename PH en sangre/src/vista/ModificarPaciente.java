    private void actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_actualizarActionPerformed
        // TODO add your handling code here:
        String edad = edadM.getText();
        String tel = telefonoM.getText();      
        String dir = direccionM.getText();
        String email = emailM.getText();
        String ec = (String)estadocivilM.getSelectedItem();
        
        String cod = codigoM.getText();
        
        if(edad.equals("") || tel.equals("") || email.equals("") || dir.equals("") || ec.equals("")) {
            JOptionPane.showMessageDialog(null, "Rellene todos los campos ", "Actualizaci√≥n inv√°lida", JOptionPane.ERROR_MESSAGE);
        } else {
            tablaPacientes = new TablaPaciente();
            JOptionPane.showMessageDialog(null, "Actualizacion Exitosa", "Registro Actualizado", JOptionPane.INFORMATION_MESSAGE);
            tablaPacientes.ModificarPaciente(cod, edad, tel, dir, email, ec);
        }
        
        this.setVisible(false);
        
    }//GEN-LAST:event_actualizarActionPerformed

